  @override
  Future<void> onLoad() async {
    await images.loadAll([
      'coins.png',
      'flaric.png',
      'lava.png',
      'peaman.png',
      'player.png',
      'wall_01.png',
    ]);

    FlameAudio.bgm.initialize();

    FlameAudio.bgm.play('snowballer.wav');

    cameraComponent = CameraComponent(
      world: world,
    );

    player = Player(position: Vector2(96, 96), gameEnder: gameEnder);

    flaric = Flaric(position: Vector2(384, 48));

    void renderMapArea(mapAreaName, mapX, mapY) {
      for (int i = 0; i < mapAreaName.length; i++) {
        for (int j = 0; j < mapAreaName[i].length; j++) {
          if (mapAreaName[i][j] == 1) {
            Wall01 tile = Wall01(
                position: Vector2(((mapX * 480) + (48 * j)).toDouble(),
                    ((mapY * 480) + (48 * i)).toDouble()));
            world.add(tile);
          } else if (mapAreaName[i][j] == 2) {
            Coins tile = Coins(
                position: Vector2(((mapX * 480) + (48 * j)).toDouble(),
                    ((mapY * 480) + (48 * i)).toDouble()));
            world.add(tile);
          } else if (mapAreaName[i][j] == 3) {
            Flaric tile = Flaric(
                position: Vector2(((mapX * 480) + (48 * j)).toDouble(),
                    ((mapY * 480) + (48 * i)).toDouble()));
            world.add(tile);
          } else if (mapAreaName[i][j] == 4) {
            Lava tile = Lava(
                position: Vector2(((mapX * 480) + (48 * j)).toDouble(),
                    ((mapY * 480) + (48 * i)).toDouble()));
            world.add(tile);
          } else if (mapAreaName[i][j] == 5) {
            Peaman tile = Peaman(
                position: Vector2(((mapX * 480) + (48 * j)).toDouble(),
                    ((mapY * 480) + (48 * i)).toDouble()));
            world.add(tile);
          }
        }
      }
    }

    // A
    renderMapArea(mapAreaA1, 0, 0);
    renderMapArea(mapAreaA2, 1, 0);
    renderMapArea(mapAreaA3, 2, 0);
    renderMapArea(mapAreaA4, 3, 0);
    renderMapArea(mapAreaA5, 4, 0);
    // B
    renderMapArea(mapAreaB1, 0, 1);
    renderMapArea(mapAreaB5, 4, 1);
    // C
    renderMapArea(mapAreaC1, 0, 2);
    renderMapArea(mapAreaC5, 4, 2);
    // D
    renderMapArea(mapAreaD1, 0, 3);
    renderMapArea(mapAreaB5, 4, 3);
    // E
    renderMapArea(mapAreaE1, 0, 4);
    renderMapArea(mapAreaE2, 1, 4);
    renderMapArea(mapAreaE3, 2, 4);
    renderMapArea(mapAreaE4, 3, 4);
    renderMapArea(mapAreaE5, 4, 4);

    // Player
    world.add(player);

    cameraComponent.follow(player);
    addAll([cameraComponent, world]);
  }
